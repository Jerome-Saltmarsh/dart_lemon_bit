
class WeaponState {
   static const Idle = 0;
   static const Performing = 1;
   static const Reloading = 2;
   static const Aiming = 3;
   static const Changing = 4;

   static String getName(int weaponState) => const {
         Idle: 'Idle',
         Reloading: 'Reloading',
         Aiming: 'Aiming',
         Changing: 'Changing',
   }[weaponState] ?? 'unknown-weapon-state-$weaponState';
}