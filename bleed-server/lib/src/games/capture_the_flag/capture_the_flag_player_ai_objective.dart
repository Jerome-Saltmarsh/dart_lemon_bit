enum CaptureTheFlagPlayerAIObjective {
  /// Take enemy flag and bring it back to the base
  Capture_Flag_Enemy,
  /// Take own flag and bring it back to base
  Capture_Flag_Own,
  /// Wait near own flag and defend it against enemies
  Defend,
  /// Attack an enemy player
  Attack,
  /// If low on health or overwhelmed,
  Heal,
  Wait,
}


